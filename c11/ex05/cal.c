#include "doop.h"

int	add(int value1, int value2)
{
	return (value1 + value2);
}

int	sub(int value1, int value2)
{
	return (value1 - value2);
}

int	mul(int value1, int value2)
{
	return (value1 * value2);
}

int	div(int value1, int value2)
{
	return (value1 / value2);
}

int	mod(int value1, int value2)
{
	return (value1 % value2);
}
